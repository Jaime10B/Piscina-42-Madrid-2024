char	*ft_strdup(char *src)
{
	int		size;
	char	*copy;
	int		i;
	
	size = sizeof(*src);
	copy = malloc(size + 1 * sizeof(char));
	if (!copy)
		return (0);
	i = 0;
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
