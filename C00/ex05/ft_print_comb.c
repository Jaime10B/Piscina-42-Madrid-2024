#include <unistd.h>

void	ft_print_comb(void)
{
	char	i;
	char	a;
	char	d;

	i = '0';
	while (i <= '7')
	{
		a = i + 1;
		while (a <= '8')
		{
			d = a + 1;
			while (d <= '9')
			{
				write(1, &i, 1);
				write(1, &a, 1);
				write(1, &d, 1);
				if (!(i == '7' && a == '8' && d == '9'))
					write(1, ", ", 2);
				d++;
			}
			a++;
		}
		i++;
	}
}
